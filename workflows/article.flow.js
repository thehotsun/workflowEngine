'use strict'

/**
 * ==============================
 * 公众号文章自动生成流程说明
 * ==============================
 * 
 * 这个流程文件定义了如何从一句话自动生成一篇完整的公众号文章。
 * 即使你不懂代码，也能通过下面的说明理解整个工作原理。
 * 
 * 整个流程就像一个专业的编辑团队：
 * 1️⃣ 先看看今天有什么热点话题
 * 2️⃣ 结合你的想法和热点，生成几个候选标题
 * 3️⃣ 从知识库中查找相关素材
 * 4️⃣ 如果知识库没有，就去网上搜索
 * 5️⃣ 选定一个话题
 * 6️⃣ 做深入调研（研究角度、关键事实、配图方向等）
 * 7️⃣ 撰写文章初稿
 * 8️⃣ 生成配图（封面图 + 文中插图）
 * 9️⃣ 排版渲染（变成漂亮的公众号格式）
 * 🔟 润色打磨
 * 1️⃣1️⃣ 准备发布
 * 
 * 下面是详细的配置和流程步骤：
 */

module.exports = {
  // 流程唯一标识（内部使用，不用管）
  id: 'article_flow',
  
  // 流程名称
  name: '公众号文章生成',

  // ==========================================
  // 🎛️ 配置区域（你可以根据需要修改这些）
  // ==========================================
  config: {
    /**
     * 🔍 知识库检索配置
     * 从本地知识库中最多取多少条相关内容
     */
    ragQuery: {
      topK: 5
    },

    /**
     * 👤 账号/品牌配置（非常重要！）
     * 这里定义了你的公众号是谁，写给谁看，风格是什么
     */
    accountProfile: {
      // 公众号的名字
      accountName: '心栖书香',
      
      // 文章末尾的作者卡片
      authorCard: {
        badge: '',
        subtitle: '陪你把家庭里的委屈、误会和边界，慢慢说清楚。',
        footer: '如果你觉得这篇文章像在说自己，也欢迎转给你惦记的那个人。',
        highlights: [
          '专注家庭关系、夫妻相处与代际沟通',
          '用温和、能共情的文字，写家里那些最难说出口的情绪',
          '愿每一篇短文，都能帮家里少一点慌张，多一点从容'
        ]
      },
      
      // 你喜欢写哪些类型的话题（系统会优先考虑）
      preferredTopics: [
        '夫妻关系',
        '家庭关系',
        '代际沟通',
        '退休生活',
        '健康提醒'
      ]
    },

    /**
     * 🔥 热点抓取配置
     * 从哪些平台抓热点，每个平台抓多少条
     */
    hotspots: {
      limitPerSource: 10,            // 每个来源最多取 10 条
      enabledSources: ['weibo', 'toutiao', 'baidu']  // 启用的来源：微博、头条、百度
    },

    /**
     * 📝 话题生成配置
     */
    topics: {
      count: 6,             // 每次生成 6 个候选话题供选择
      avoidDuplicates: true // 避免重复之前写过的话题
    },

    /**
     * ✍️ 文章写作配置
     */
    write: {
      targetWordCount: { min: 900, max: 1400 }, // 文章字数在 900-1400 之间
      digestLength: 54                          // 摘要最多 54 个汉字
    },

    /**
     * 🖼️ 图片配置
     */
    images: {
      // 在文章的哪些位置放图
      enabledSlots: [
        'after_lead',       // 引言后面
        'after_section_1',  // 第一小节后面
        'after_section_2',  // 第二小节后面
        'before_ending'     // 结尾前面
      ],
      // 如果 AI 没生成封面图提示词，用这个默认的
      coverPromptFallback: '温暖、真实的中老年家庭生活场景，光线柔和，横版，适合公众号封面'
    }
  },

  // ==========================================
  // 🎯 触发条件（什么时候启动这个流程）
  // ==========================================
  trigger: {
    type: 'message',
    // 当用户说这些话时，就会自动触发这个流程
    match: /写文章|写公众号|帮我写|生成文章/
  },

  // ==========================================
  // 📋 步骤详解（一步一步来）
  // ==========================================
  steps: [
    // --------------------------------------------------
    // 第 1 步：抓取今日热点
    // 去微博、头条、百度看看今天大家都在讨论什么
    // --------------------------------------------------
    {
      type: 'fetch-hotspots'
    },

    // --------------------------------------------------
    // 第 2 步：生成候选话题
    // 结合用户输入 + 热点 + 账号偏好，生成 6 个候选标题
    // --------------------------------------------------
    {
      type: 'generate-topics'
    },

    // --------------------------------------------------
    // 第 3 步：查找知识库
    // 从本地知识库中搜索相关素材，让文章有料
    // --------------------------------------------------
    {
      type: 'rag-query',
      input: ctx => ({ query: ctx.get('topics')?.[0]?.title || ctx.get('input') }),
      output: 'ragResults'
    },

    // --------------------------------------------------
    // 第 4 步：如果知识库没东西，就去网上搜
    // 智能判断：有知识库用知识库，没有就去网上搜索
    // --------------------------------------------------
    {
      type: 'conditional',
      condition: ctx => {
        const r = ctx.get('ragResults')
        return !Array.isArray(r) || r.length === 0
      },
      ifTrue: {
        type: 'skill-proxy',
        skill: 'web-search',
        input: ctx => ({ 
          query: ctx.get('topics')?.[0]?.title || ctx.get('input'),
          count: 5
        }),
        output: 'searchResults',
        timeout: 20000
      },
      ifFalse: { type: 'noop' }
    },

    // --------------------------------------------------
    // 第 5 步：选定话题
    // 从 6 个候选中选一个最好的（或者你可以自己选）
    // --------------------------------------------------
    {
      type: 'select-topic'
    },

    // --------------------------------------------------
    // 第 6 步：做调研
    // 深入研究：文章角度、关键事实、写法提示、配图方向等
    // --------------------------------------------------
    {
      type: 'research'
    },

    // --------------------------------------------------
    // 第 7 步：写文章
    // 基于所有准备工作，生成完整的文章（带结构、带配图提示）
    // --------------------------------------------------
    {
      type: 'write'
    },

    // --------------------------------------------------
    // 第 8 步：生成配图
    // 根据提示词，生成封面图和文中插图
    // --------------------------------------------------
    {
      type: 'image-generate'
    },

    // --------------------------------------------------
    // 第 9 步：排版渲染
    // 把文章变成漂亮的 HTML/Markdown 格式，加上作者卡片
    // --------------------------------------------------
    {
      type: 'render-article'
    },

    // --------------------------------------------------
    // 第 10 步：润色
    // 最后打磨一下文字
    // --------------------------------------------------
    {
      type: 'polish'
    },

    // --------------------------------------------------
    // 第 11 步：准备发布
    // 把所有东西打包好，可以直接发布了
    // --------------------------------------------------
    {
      type: 'publish'
    }
  ],

  // ==========================================
  // ⚠️ 出错怎么办？
  // ==========================================
  onError: 'notify-and-dlq'
}
